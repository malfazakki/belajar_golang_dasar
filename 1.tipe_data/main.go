package main

import "fmt"

func main() {
	var angka int = 42                 // Bilangan Bulat
	var desimal float64 = 3.14         // Bilangan Desimal
	var teks string = "Golang is Good" // String (teks)
	var benar bool = true              // Boolean

	fmt.Println("Angka: ", angka)
	fmt.Println("Desimal: ", desimal)
	fmt.Println("Teks: ", teks)
	fmt.Println("Benar: ", benar)
}
